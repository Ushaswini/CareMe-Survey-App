        [AllowAnonymous]
        [Route("UserIsAdim")]
        public IHttpActionResult GetUserForRole(string roleName, string userName)
        {
            var adminRole = AppRoleManager.Roles.Single(r => r.Name == roleName);
            var user = UserManager.Users.Where(u => u.Roles.Any(r => r.RoleId == adminRole.Id) && u.UserName == userName).ToList();
            if (user.Count > 0)
            {
                return Ok();
            }
            return NotFound();
        }

        [Route("UserInfo")]
        public UserInfoViewModel GetUserInfo(string token)
        {
            JwtSecurityToken tok = new JwtSecurityToken(token);
            string currentUserId = tok.Claims.Where(x => x.Type == "unique_name").FirstOrDefault().Value;
            var manager = new UserManager<ApplicationUser>(new UserStore<ApplicationUser>(new ApplicationDbContext()));
            var currentUser = manager.FindById(currentUserId);
            var model = new UserInfoViewModel
            {
                Email = currentUser.Email,
                Roles = UserManager.GetRoles(currentUser.Id),            
                Id = currentUser.Id,
                UserName = currentUser.UserName
            };
            return model;
        }


